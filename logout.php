session_start();
$domain = ($_SERVER['HTTP_HOST'] != 'localhost') ? $_SERVER['HTTP_HOST'] : false;
if(session_status() === PHP_SESSION_ACTIVE and isset($_COOKIE['login_email']) and isset($_COOKIE['login_password'])){
    $_SESSION = array();

    if (ini_get("session.use_cookies")) {
        $params = session_get_cookie_params();
        setcookie(session_name(), '', time() - 42000,
            $params["path"], $params["domain"],
            $params["secure"], $params["httponly"]
        );
    }

    session_destroy();
    setcookie('login_email', FALSE, time()-100, '/', $domain, false);
    //setcookie('login_email', "", time()-1);
    setcookie('login_password', FALSE, time()-100, '/', $domain, false);
    header("Location: index.php");
    //echo"If triggered";
}else{
    $_SESSION = array();

